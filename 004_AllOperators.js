// Arithmetic Operators
let sum = 5 + 3; // sum is 8
let difference = 10 - 4; // difference is 6
let product = 2 * 6; // product is 12
let quotient = 8 / 2; // quotient is 4
let remainder = 11 % 3; // remainder is 2

// Assignment Operators
let x = 10; // assigns the value 10 to x
let y = 5;
y += 3; // equivalent to y = y + 3, y is now 8
let z = 7;
z -= 4; // equivalent to z = z - 4, z is now 3

// Comparison Operators
let isEqual = 10 == 10; // isEqual is true
let isStrictEqual = '5' === 5; // isStrictEqual is false
let isNotEqual = 10 != 5; // isNotEqual is true

// Logical Operators
let isBothTrue = true && false; // isBothTrue is false
let isEitherTrue = true || false; // isEitherTrue is true
let isNotTrue = !true; // isNotTrue is false

// Unary Operators
let positiveNumber = +5; // positiveNumber is 5
let negativeNumber = -3; // negativeNumber is -3
let notTrue = !true; // notTrue is false

// Conditional (Ternary) Operator
let result = condition ? valueIfTrue : valueIfFalse;

// typeof Operator
let variableType = typeof "Hello"; // variableType is "string"

